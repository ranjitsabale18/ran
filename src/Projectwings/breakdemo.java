package Projectwings;

public class breakdemo {

	public static void main(String[] args) {
		for(int i=0; i<7; i++) 
		{
			if (i==4)
			{
				break;
			}
			System.out.println(i);
		}
		
		System.out.println(">>>>>>>>>>>>>>>>>>>>>>>>>>>>");
	
		
		for(int i=0; i<11; i++)
		{
			if (i==6)
			{
				continue;
			}
			System.out.println(i);
		}
	}
	
		   
	}




